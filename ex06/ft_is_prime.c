int	ft_is_prime(int n)
{
	int	i;

	if (n < 2)
		return (0);
	else if (n == 2)
		return (1);
	else
	{
		i = 2;
		while (i <= n / 2)
		{
			if (n % i == 0)
				return (0);
			i++;
		}
		return (1);
	}
}
